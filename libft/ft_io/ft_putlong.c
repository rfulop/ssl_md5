#include "libft.h"

void	ft_putlong(long long int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n <= 9)
		ft_putchar(n + '0');
	else
	{
		ft_putlong(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
