#include "libft.h"

int		ft_findstr(char *s1, char *s2)
{
	int a;

	a = 0;
	while (s1[a])
	{
		if (s1[a] == s2[0])
		{
			if (!(ft_strncmp(s1 + a, s2, (int)ft_strlen(s2))))
				return (1);
		}
		a++;
	}
	return (0);
}
