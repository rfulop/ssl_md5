#include "libft.h"

char	*ft_strrchr(const char *src, int c)
{
	int a;

	a = ft_strlen(src);
	while (a >= 0)
	{
		if (src[a] == c)
			return ((char *)src + a);
		a--;
	}
	return (NULL);
}
