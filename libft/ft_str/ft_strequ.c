#include "libft.h"

int		ft_strequ(const char *s1, const char *s2)
{
	int	a;

	if (s1 && s2)
	{
		a = 0;
		while (s1[a] || s2[a])
		{
			if (s1[a] != s2[a])
				return (0);
			++a;
		}
	}
	return (1);
}
