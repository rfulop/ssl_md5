#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	unsigned char	*str_b;
	size_t			a;

	a = 0;
	str_b = (unsigned char*)str;
	while (a < n)
	{
		str_b[a] = 0;
		++a;
	}
}
