#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int a, size_t n)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	i = 0;
	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	while (i < n)
	{
		d[i] = s[i];
		if (d[i] == (unsigned char)a)
			return (dest + i + 1);
		++i;
	}
	return (NULL);
}
