#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char	*temp;

	temp = (unsigned char *)ft_strsub(src, 0, n);
	ft_memcpy(dest, temp, n);
	free(temp);
	return (dest);
}
